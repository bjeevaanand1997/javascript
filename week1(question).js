let f = 23.45;

// Method 1: Using Math.floor() (round down)
let int1 = Math.floor(f); // 23

// Method 2: Using Math.ceil() (round up)
let int2 = Math.ceil(f); // 24

// Method 3: Using Math.round() (nearest integer)
let int3 = Math.round(f); // 23

// Method 4: Using parseInt() (truncates toward zero)
let int4 = parseInt(f); // 23

console.log(int1, int2, int3, int4);

